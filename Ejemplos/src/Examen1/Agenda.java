package Examen1;

public class Agenda {

	private String nombre;
	private int telefono;
	private int tamaño;
	/**
	 * 
	 */
	public Agenda() {
		super();
	}
	/**
	 * @param nombre
	 * @param telefono
	 * @param tamaño
	 */
	public Agenda(String nombre, int telefono, int tamaño) {
		super();
		this.nombre = nombre;
		this.telefono = telefono;
		this.tamaño = tamaño;
	}
	
	
	
}
